          getSlug() {
               let characters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789".split("");
               let result = "";

               for (let i = 0; i < 6; i++) {
                    result += characters[Math.floor(Math.random() * characters.length)];
               }

               return result;
          }
